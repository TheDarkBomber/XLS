    { pattern = "-?[0-9%.]+",                              type = "number"   },
    { pattern = "-?%.?%d+",                                type = "number"   },
    { pattern = "[%a_][%w_%-+/*%%&:@=><?!]* *%f[(]",       type = "function" },
    { pattern = "%f[|]%f[>] *[%a_][%w_%-+/*%%&:@=><?!]*",  type = "function" },
    { pattern = "[%a_][%w_%-+/*%%&:@=><?!]*%f[%*]",        type = "symbol"   },
    { pattern = "[%a_][%w_%-+/*%%&:@=><?!]*",              type = "symbol"   },
  },
  symbols = {
    ["implement"]  = "keyword",
    ["impl"]       = "keyword",
    ["extern"]     = "keyword",
    ["if"]         = "keyword",
    ["else"]       = "keyword",
