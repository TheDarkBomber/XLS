    ["alias"]      = "keyword",
    ["break"]      = "keyword",
    ["continue"]   = "keyword",
    ["return"]     = "keyword",
    ["c-variadic"] = "keyword",
    ["variadic"]   = "keyword",
    ["setjump"]    = "keyword",
    ["longjump"]   = "keyword",
    ["funcdef"]    = "keyword",
    ["macro"]      = "keyword",
    ["dword"]      = "keyword2",
    ["word"]       = "keyword2",
    ["byte"]       = "keyword2",
    ["boole"]      = "keyword2",
    ["void"]       = "keyword2",
    ["sdword"]     = "keyword2",
    ["sword"]      = "keyword2",
    ["sbyte"]      = "keyword2",
    ["jumpbuf"]    = "keyword2",
    ["valist"]     = "keyword2",
    ["fn&"]        = "keyword2",
    ["label&"]     = "keyword2",
  }
}
