  files = {"%.xls$"},
  comment = "//",
  patterns = {
    { pattern = { "//", "\n" },                            type = "comment"  },
    { pattern = { "#", "\n"},                              type = "comment"  }, -- there is no standardised preprocessor colour unfortunately
    { pattern = { '"', '"', '\\' },                        type = "string"   },
    { pattern = "'\\?[^']'",                               type = "string"   },
    { pattern = "-?0b[01]+",                               type = "number"   },
    { pattern = "-?0q[0-3]+",                              type = "number"   },
